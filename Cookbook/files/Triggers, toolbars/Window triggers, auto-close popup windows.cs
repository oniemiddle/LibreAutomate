/// To execute some code or run a script, can be used window <see cref="Au.Triggers.ActionTriggers">triggers<>. For example auto-close popup window X whenever it appears.
///
/// To add a trigger can be used snippet <.x>triggerSnippet<> or `Ctrl+Shift+Q` or menu <b>TT > New trigger<>. Add triggers in function <.x>WindowTriggers<> in file `Window triggers`; you'll find examples there.
///
/// To open a triggers file for editing can be used the <b>TT<> menu.
/// 
/// Click the <b>Run<> button to apply changes after editing.
/// 
/// Tips:
/// - Most window trigger parameters are the same as with <.x>wnd.find<>. To get window name etc can be used the <b>Find window<> tool or Quick capturing (`Ctrl+Shift+Q`).
/// - To get code for "run script" or "run/open file or URL" you can drag and drop scripts, files and links to the code editor.
/// 
/// See also recipe <+recipe>Triggers and toolbars<>.
///
/// Also triggers can be used in any script. For example in an `.exe` program that runs without the editor. <see cref="Au.Triggers.ActionTriggers">Examples<>.
