/// Autotext <see cref="Au.Triggers.ActionTriggers">triggers<> can be used to execute some code or auto-replace text when you type an abbreviation or an incorrectly spelled word. Also known as text expanding, auto-correction, hotstrings.
///
/// To add a trigger can be used snippet <.x>triggerSnippet<> or menu <b>TT > New trigger<>. Add triggers in function <.x>AutotextTriggers<> in file `Autotext triggers`; you'll find examples there.
///
/// To open a triggers file for editing can be used the <b>TT<> menu.
/// 
/// Click the <b>Run<> button to apply changes after editing.
/// 
/// Tips:
/// - To get code for "run script" or "run/open file or URL" you can drag and drop scripts, files and links to the code editor.
/// - To quickly insert `Triggers.Of.Window(...)` code, use the quick capturing hotkey (default `Ctrl+Shift+Q`).
/// 
/// See also recipe <+recipe>Triggers and toolbars<>.
///
/// Also triggers can be used in any script. For example in an `.exe` program that runs without the editor. <see cref="Au.Triggers.ActionTriggers">Examples<>.
